    private void exibirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exibirActionPerformed
    if(tam12.isSelected() == true){
        tamfonte = 12;
    }else if(tam14.isSelected() == true){
        tamfonte = 14;
    }else if(tam16.isSelected() == true){
        tamfonte = 16;
    }else if(tam18.isSelected() == true) {
        tamfonte = 18;
    }else{
        tamfonte = 12;
    }
    
    String cor = combo.getSelectedItem().toString();
    /*nehum
    vermelho
    azul
    amarelo
    verde
    preto*/
    if("vermelho".equals(cor)){
        vazio.setForeground(Color.RED);
    }
    
        
    vazio.setFont(new Font("", estilo, tamfonte));
    
    if(selecao.isSelected() == true){
        vazio.setText("A cidade escolhida foi: " + selecao.getText());
    }
    
    if(selecao1.isSelected() == true){
    vazio.setText("A cidade escolhida foi: " + selecao1.getText());
    }
    
    if(selecao2.isSelected() == true){
    vazio.setText("A cidade escolhida foi: " + selecao2.getText());
    }
    
    if(selecao3.isSelected() == true){
    vazio.setText("A cidade escolhida foi: " + selecao3.getText());
    }
    
    if(selecao4.isSelected() == true){
    vazio.setText("A cidade escolhida foi: " + selecao4.getText());
    }
    }//GEN-LAST:event_exibirActionPerformed

